fun getTranslations(lang: Language) = translationsMap[lang] ?: translationsMap[Language.EN]!!

fun Translations.localizeProduct(name: String): String {
    return when (name.lowercase().replace(" ", "")) {
        "bag" -> productbag
        "keychain" -> productkeychain
        "earrings" -> productearrings
        else -> name
    }
}

fun Translations.localizeColor(name: String): String {
    return when (name.lowercase().replace(" ", "")) {
        "red" -> colorred
        "blue" -> colorblue
        "yellow" -> coloryellow
        "green" -> colorgreen
        "black" -> colorblack
        "white" -> colorwhite
        "orange" -> colororange
        "purple" -> colorpurple
        "pink" -> colorpink
        "cyan" -> colorcyan
        "gray" -> colorgray
        "clay" -> colorclay
        else -> name
    }
}
